package com.teller.pixeldungeonofteller.actors;

public class PhysicalPercentage {
    public float IMPACTPENCENTAGE = 0f;
    public float SLASHPENCENTAGE = 0f;
    public float PUNCTUREPENCENTAGE = 0f;

    public PhysicalPercentage(float a, float b, float c) {
        IMPACTPENCENTAGE = a;
        SLASHPENCENTAGE = b;
        PUNCTUREPENCENTAGE = c;
    }
}